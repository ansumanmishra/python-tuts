str1 = "this is a sample text"

# Length of a string
print(len(str1))

# Replace a word/character
print(str1.replace('text', 'line'))

# Strip white space
print(str1.strip())

# Capitalize
print(str1.capitalize())

# Substring - Get first 4 characters
print(str1[0:4])

# Get last 2 characters
print(str1[-2:])

# Strip last two characters
print(str1[0:-2])

# Split text by space
print(str1.split(' ')) # Creates a list